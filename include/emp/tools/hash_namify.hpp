  std::string hash_namify( const size_t hash ) {

    const size_t num_adjectives = internal::adjectives.size();
    const size_t num_animals = internal::animals.size();

    const size_t adjective_idx = ( hash / num_animals ) % num_adjectives;
    const size_t animal_idx = hash % num_animals;

    return emp::to_string(
      emp::internal::adjectives[adjective_idx],
      " ",
      emp::internal::animals[animal_idx]
    );

  }

